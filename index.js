function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

function increment(i) {
    return i += 1;
}

function decrement(i) {
    return i -= 1;
}

function makeInt(i) {
    return parseInt(i, 10);
}

function preserveDecimal(i) {
    return parseFloat(i);
}